    است.</p><p>پشتیبانی MVC و رویکرد شی گرا</p><p>اولین و بهترین مزیت استفاده از فریم ورک لاراول این است که از الگوی
    معماری مبتنی بر مدل، نمایش و کنترلر پیروی می کند و یک نحو زیبا بیانگر دارد که آن را شی گرا می کند.</p><p>احراز هویت
    داخلی و مجوز</p><p>لاراول یک پیکربندی خارج از جعبه برای سیستم احراز هویت و مجوز ارائه می دهد. یعنی تنها در چند دستور
    artisan اپلیکیشن شما به Authentication و Authorization ایمن مجهز می شود.</p><p><br></p><p>سیستم بسته بندی</p><p>یک
    سیستم بسته بندی با چندین نرم افزار یا کتابخانه های پشتیبانی سروکار دارد که به برنامه کاربردی وب کمک می کند تا فرآیند
    را خودکار کند. لاراول از یک آهنگساز به عنوان مدیر وابستگی استفاده می کند که تمام اطلاعات مورد نیاز برای مدیریت بسته
    ها را مدیریت می کند. بسته ها یک راه عالی برای سرعت بخشیدن به توسعه هستند، ارائه عملکردی که ما به آن نیاز داریم.

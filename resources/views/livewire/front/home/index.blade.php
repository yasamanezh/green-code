<p>فریمورک لاراول</p><p>ابتدا با مفهوم فروم ورک آشنا میشویم و سپس فریم ورک لاراول را توضیح میدهیم.</p><p>فریم ورک
    چیست؟</p><p>در اصل فریم ورک یا framework در لغت به معنی چارچوب است که مجموعه ای از قوانین کدنویسی، استانداردها،
    کتابخانه ها و دیزاین پترن ها است و به صورت یک برنامه یا چارچوب نرم افزاری در اختیار برنامه نویس ها و توسعه دهنده ها
    قرار گرفته است.</p><p>&nbsp;تیلور اوتوِل (Taylor Otwell) خالق فریم ورک Laravel است. اولین نسخه این فریم ورک در سال
    ۲۰۱۱ در اختیار توسعه دهندگان قرار گرفت. این فریم ورک در بین سایر فریم ورک‌های PHP از امنیت و سرعت توسعه‌ی بیشتری
    برخوردار است و از طریق آن شما قادر خواهید بود بدون اینکه تغییری در کدهای اصلی ایجاد کنید، عملکرد بسیاری از بخش‌ها را
    تغییر دهید. لاراول کتابخانه‌های بسیار متنوعی دارد و از اغلب کتابخانه‌های فریم ورک سیمفونی نیز استفاده می‌کند. همچنین
    این فریم ورک سرویس‌های مختلفی را برای برنامه نویسان ارائه می‌دهد. به عنوان مثال سرویس هایی همچون تأیید هویت،
    دسترسی‌های کاربران، کنسول Artisan، پرداخت و صورتحساب، کش (Cache) و … که تنها بخش کوچکی از سرویس هایی است که این فریم
    ورک در اختیار برنامه‌ نویسان قرار می‌دهد.</p>

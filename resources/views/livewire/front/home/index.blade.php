    لاراول همچنین دارای پشتیبانی داخلی برای سیستم ذخیره سازی ابری مانند Amazon S3 و Rack space Cloud Storage و البته
    برای ذخیره سازی محلی است. جابجایی بین این گزینه های ذخیره سازی به طرز شگفت انگیزی ساده است زیرا API برای هر سیستم
    یکسان می ماند. می توان از هر سه سیستم در یک برنامه برای ارائه فایل ها از مکان های مختلف مانند یک محیط توزیع شده
    استفاده کرد.</p><p>خط فرمان&nbsp; اختصاصی</p><p>لاراول دارای رابط خط فرمان خود به نام Artisan است. کاربردهای رایج
    Artisan شامل انتشار دارایی های بسته، مدیریت مهاجرت های پایگاه داده، کاشت و تولید کد boilerplate برای کنترلرها، مدل
    ها و مهاجرت های جدید است. این ویژگی توسعه دهنده را از ایجاد اسکلت کد مناسب رها می کند. می توان با اجرای دستورات
    سفارشی جدید، عملکرد و قابلیت های Artisan را گسترش داد.</p><p>Eloquent ORM&nbsp;</p><p>Eloquent ORM پیاده سازی ORM
